  <!-- Types of MICE tourism -->
  <div class="space-y-4">
    <h2 class="text-2xl font-semibold text-gray-900">Common types of MICE tourism</h2>

    <h3 class="text-xl font-semibold text-gray-800">Meeting tours</h3>
    <p>These programs combine business meetings or internal conferences with leisure travel, usually set in premium resorts. This setting fosters effective communication and team
      cohesion, making it ideal for annual reviews, customer conferences, or strategic planning sessions.</p>

    <h3 class="text-xl font-semibold text-gray-800">Incentive tours</h3>
    <p>Designed as rewards for outstanding employees or partners, these trips boost morale and encourage loyalty. Programs often include engaging activities such as creative
      teambuilding sessions or elegant gala dinners to create a sense of recognition and appreciation.</p>

    <h3 class="text-xl font-semibold text-gray-800">Convention tours</h3>
    <p>Tailored for participants attending large-scale industry conventions or international seminars, these tours demand high standards in accommodation, transportation, and
      logistics. With multi-day agendas and renowned speakers, they promote professional development and networking on a global scale.</p>

    <h3 class="text-xl font-semibold text-gray-800">Event/Exhibition tours</h3>
    <p>These tours combine attendance at trade shows, festivals, or international exhibitions with local sightseeing and cultural immersion. They are particularly effective for
      brand promotion, market exploration, and connecting with potential business partners.</p>
  </div>

