  <h3 class="text-xl font-semibold mt-4 mb-2">Meeting tour – Du lịch hội họp</h3>
  <p class="text-lg">Loại hình kết hợp các buổi họp, hội thảo với chương trình du lịch nghỉ dưỡng. Thường tổ chức tại các khu resort cao cấp, tạo không gian trao đổi hiệu quả và
