  <!-- Day 1 -->
  <div class="space-y-4">
    <h2 class="text-2xl font-semibold text-gray-800">DAY 1: SON TRA – MY KHE BEACH – HOI AN (Lunch, Dinner)</h2>
    <p><span class="font-semibold">Morning:</span> Pick-up at Da Nang airport/train station (7:00 to 12:00). Early arrivals can enjoy breakfast or coffee (self-paid), then join the
      group.</p>
    <p><span class="font-semibold">Noon:</span> Lunch at a local restaurant. Visit Son Tra Peninsula (Monkey Mountain), Linh Ung Pagoda, and the 67-meter-tall Lady Buddha statue.
      Relax at My Khe Beach, one of the world's top 6 most alluring beaches (Forbes).</p>
    <p><span class="font-semibold">Afternoon:</span> Travel to Hoi An Ancient Town and visit:</p>
    <ul class="list-disc list-inside pl-4 space-y-1">
      <li>Chua Cau (Pagoda Bridge)</li>
      <li>Cantonese or Fujian Assembly Hall</li>
      <li>Tan Ky or Phung Hung Ancient House</li>
    </ul>
    <p>Wander through the lantern-lit streets of the old town.</p>
    <p><span class="font-semibold">Evening:</span> Dinner with Hoi An specialties (cao lau, wonton, chicken rice...). Return to Da Nang via coastal route. Overnight stay in Da
      Nang.</p>
  </div>

  <!-- Day 2 -->
  <div class="space-y-4">
    <h2 class="text-2xl font-semibold text-gray-800">DAY 2: BA NA HILLS – GOLDEN BRIDGE (Breakfast, Dinner)</h2>
    <p><span class="font-semibold">Morning:</span> Breakfast at hotel. Depart for Ba Na Hills. Ride the Guinness World Record cable car to visit:</p>
    <ul class="list-disc list-inside pl-4 space-y-1">
      <li>Mo Spring, 100-year-old wine cellar</li>
      <li>Golden Bridge, Linh Ung Pagoda, Vong Nguyet Hill</li>
      <li>Thich Ca Buddha statue, ethnic stilt houses</li>
      <li>Nghinh Phong Peak, 1,487m summit</li>
      <li>Fantasy Park (games at own expense)</li>
    </ul>
    <p><span class="font-semibold">Noon:</span> Buffet lunch at Ba Na restaurant (if not registered, meals are self-paid).</p>
    <p><span class="font-semibold">Afternoon:</span> Return to Da Nang via Nguyen Tat Thanh coastal road, passing Thuan Phuoc Bridge, Thanh Binh Beach, and Red Beach. Stop at a
      specialty supermarket.</p>
    <p><span class="font-semibold">Evening:</span> Dinner at a local restaurant. Free to explore Da Nang nightlife: Han River cruise, Sun Wheel, night food streets, bars, cafés.
      Overnight stay in Da Nang.</p>
  </div>

  <!-- Day 3 -->
  <div class="space-y-4">
    <h2 class="text-2xl font-semibold text-gray-800">DAY 3: MARBLE MOUNTAINS – DEPARTURE (Breakfast, Lunch)</h2>
    <p><span class="font-semibold">Morning:</span> Breakfast at hotel. Visit Marble Mountains (10km from city center) and explore:</p>
    <ul class="list-disc list-inside pl-4 space-y-1">
      <li>Xa Loi Tower, Linh Ung Pagoda 1</li>
      <li>Tang Chon, Van Thong, Huyen Khong Caves</li>
      <li>Vong Giang Tower, Tam Thai Pagoda</li>
    </ul>
    <p>Descend the mountain by foot. Visit Non Nuoc Stone Carving Village and key Da Nang landmarks:</p>
    <ul class="list-disc list-inside pl-4 space-y-1">
      <li>Administrative Center Tower</li>
      <li>Han River Swing Bridge</li>
      <li>Dragon Bridge, Love Bridge, Carp Turns Into Dragon Statue</li>
    </ul>
    <p>Shop for local specialties at Han Market or supermarkets.</p>
    <p><span class="font-semibold">Noon:</span> Lunch with Da Nang specialties: rice paper rolls with pork & fermented fish noodle soup.</p>
    <p><span class="font-semibold">Afternoon:</span> Transfer to Da Nang airport/train station (14:30–17:30). Guests departing after this time arrange their own transport. Tour
      ends.</p>
  </div>

