  <h3 class="text-xl font-semibold mt-4 mb-2">NGÀY 01: SƠN TRÀ – MỸ KHÊ – HỘI AN <span class="text-sm">(ăn trưa, tối)</span></h3>
  <div class="space-y-2">
    <p><strong>Sáng:</strong> Đón khách tại sân bay/ga Đà Nẵng (7h–12h), nếu đến sớm có thể đi ăn sáng cà phê (tự túc), sau đó ghép đoàn.</p>
    <p><strong>Trưa:</strong> Ăn trưa tại nhà hàng. Khởi hành khám phá Bán đảo Sơn Trà (Monkey Mountain) – viếng chùa Linh Ứng, chiêm ngưỡng tượng Quan Âm 67m, sau đó dạo chơi biển
      Mỹ Khê (Forbes xếp vào 6 bãi biển quyến rũ nhất thế giới).</p>
    <p><strong>Chiều:</strong> Đến Hội An – tham quan chùa Cầu Nhật Bản, Hội Quán Quảng Đông/Phúc Kiến, nhà cổ Tấn Ký/Phùng Hưng, phố đèn lồng lung linh.</p>
    <p><strong>Tối:</strong> Ăn tối đặc sản Hội An (cao lầu, hoành thánh, cơm gà…), trở về Đà Nẵng theo đường biển và nghỉ đêm tại Đà Nẵng.</p>
  </div>

  <h3 class="text-xl font-semibold mt-6 mb-2">NGÀY 02: BÀ NÀ HILLS – CẦU VÀNG <span class="text-sm">(ăn sáng, tối)</span></h3>
  <div class="space-y-2">
    <p><strong>Sáng:</strong> Ăn sáng tại khách sạn. Khởi hành đến Bà Nà Hills – lên cáp treo (đạt kỷ lục Guinness), khám phá suối Mơ, tham quan hầm rượu 100 năm, cầu Vàng, đồi
      Vọng Nguyệt, chùa Linh Ứng, Thích Ca Phật Đài, khu nhà rông, đến đỉnh Nghinh Phong, đỉnh Chúa 1.487m, và vui chơi tại Fantasy Park (chi phí trò chơi tự túc).</p>
    <p><strong>Trưa:</strong> Ăn buffet tại nhà hàng Bà Nà (nếu không đăng ký buffet sẽ tự túc).</p>
    <p><strong>Chiều:</strong> Trở về Đà Nẵng qua tuyến biển Nguyễn Tất Thành – ngắm cầu Thuận Phước, bãi biển Thanh Bình, Red Beach, ghé siêu thị đặc sản miền Trung.</p>
    <p><strong>Tối:</strong> Ăn tối tại nhà hàng, sau đó tự do khám phá Đà Nẵng về đêm: du thuyền sông Hàn, vòng quay Mặt Trời, phố ẩm thực, bar, café. Nghỉ đêm tại Đà Nẵng.</p>
  </div>

  <h3 class="text-xl font-semibold mt-6 mb-2">NGÀY 03: NGŨ HÀNH SƠN – TIỄN KHÁCH <span class="text-sm">(ăn sáng, trưa)</span></h3>
  <div class="space-y-2">
    <p><strong>Sáng:</strong> Ăn sáng. Khởi hành đi Ngũ Hành Sơn (cách trung tâm 10 km), tham quan Tháp Xá Lợi, chùa Linh Ứng 1, động Tàng Chơn, Vân Thông, Huyền Không quan, Vọng
      Giang Đài, chùa Tam Thai, sau đó xuống núi bằng đường bộ.</p>
    <p>Thăm làng đá mỹ nghệ Non Nước.</p>
    <p>Tham quan các điểm nổi bật của Đà Nẵng: tòa nhà hành chính, cầu quay sông Hàn, cầu Rồng, cầu Tình Yêu, tượng Cá Chép hóa Rồng, mua đặc sản tại chợ Hàn/supermarket.</p>
    <p><strong>Trưa:</strong> Ăn trưa tại nhà hàng với đặc sản Đà Nẵng: “Bánh tráng cuốn thịt heo & Bún mắm”.</p>
    <p><strong>Chiều:</strong> Xe tiễn khách ra sân bay/ga Đà Nẵng (14h30–17h30). Sau thời gian này khách tự túc. Kết thúc chương trình.</p>
  </div>

